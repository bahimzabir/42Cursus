#include "../include/minitalk.h"

void	ft_putnbr(int n)
{
	unsigned int	r;
	char			*c;

	c = "0123456789";
	if (n < 0)
	{
		r = -n;
		write(1, "-", 1);
	}
	else
		r = n;
	if (r >= 10)
	{
		ft_putnbr(r / 10);
		write(1, &c[r % 10], 1);
	}
	else
		write(1, &c[r % 10], 1);
}
