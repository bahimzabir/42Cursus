#include "ft_printf.h"

void	ft_putstr_count(char *c, int *count)
{
	if (c == NULL)
	{
		write(1, "(null)", 6);
		*count += 6;
	}
	else
	{
		while (*c)
		{
			write(1, &*c, 1);
			c++;
			*count += 1;
		}
	}
}
