#include "../philo.h"

int *ft_philos(int	nof)
{
	int *i;
	int	j;

	i = malloc(sizeof(int) * nof);
	j = 0;
	while(j < nof)
	{
		i[j] = j + 1;
		j++;
	}
	return (i);
}
