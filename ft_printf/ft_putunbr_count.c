#include "ft_printf.h"

void	ft_putunbr_count(unsigned int n, int *count)
{
	char	*c;

	c = "0123456789";
	if (n > 9)
	{
		ft_putnbr_count(n / 10, count);
	}
	*count += 1;
	write(1, &c[n % 10], 1);
}
